package Aula04;

public class Questao3 {
    public static void main(String[] args) {
        //Usando While
        int i = 101;
        while(i <= 110) {
            System.out.println(i);
            i++;
        }

        System.out.println("***********************");

        //Usando For
        for(int j = 101; j <= 110; j++) {
            System.out.println(j);
        }
    }
}
