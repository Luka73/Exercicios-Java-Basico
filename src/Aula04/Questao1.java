package Aula04;

public class Questao1 {
    public static void main(String[] args) {
        //Usando While
        int i = 0;
        while(i <= 30) {
            System.out.println(i);
            i++;
        }

        System.out.println("***********************");

        //Usando For
        for(int j = 0; j <= 30; j++) {
            System.out.println(j);
        }
    }
}
