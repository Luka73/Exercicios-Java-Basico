package Aula04;

public class Questao4 {
    public static void main(String[] args) {
        //Usando While
        int i = 15;
        while(i >= 1) {
            System.out.println(i);
            i--;
        }

        System.out.println("***********************");

        //Usando o For
        for(int j = 15; j >= 1; j--) {
            System.out.println(j);
        }
    }
}
